/** Tooltip label for a stub public holiday, or null if unknown. */
export function getStubPublicHolidayName(market: string, isoDate: string): string | null {
  const row = LABELS_BY_MARKET[market];
  if (!row) return null;
  return row[isoDate] ?? null;
}
