/**
 * Stub public holiday **names** for tooltips (same dates as auto lists in `holidayCalc.ts`).
 * English labels for UI; not legal calendars.
 */

export type PublicHolidayEntry = { readonly date: string; readonly name: string };

function uniqSort(dates: string[]): string[] {
  return [...new Set(dates)].sort();
}

export function entriesToDates(entries: readonly PublicHolidayEntry[]): string[] {
  return uniqSort(entries.map((e) => e.date));
}

export function entriesToLabelMap(entries: readonly PublicHolidayEntry[]): Record<string, string> {
  const m: Record<string, string> = {};
  for (const { date, name } of entries) {
    m[date] = m[date] ? `${m[date]} · ${name}` : name;
  }
  return m;
}

