/**
 * Emits `server/impl/_capacityWorkspaceAcl.data.ts` from `public/data` so the shared-dsl bundle
 * never reads the filesystem at runtime (avoids Vercel FUNCTION_INVOCATION_FAILED).
 */
import { readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = path.join(path.dirname(fileURLToPath(import.meta.url)), '..');
const segmentsPath = path.join(repoRoot, 'public/data/segments.json');
const manifestPath = path.join(repoRoot, 'public/data/markets/manifest.json');
const outPath = path.join(repoRoot, 'server/impl/_capacityWorkspaceAcl.data.ts');

function load() {
  const rawSeg = JSON.parse(readFileSync(segmentsPath, 'utf8'));
  /** @type {Record<string, readonly string[]>} */
  const segmentToMarkets = {};
  for (const [k, v] of Object.entries(rawSeg)) {
    const code = k.trim().toUpperCase();
    if (!code || !Array.isArray(v)) continue;
    segmentToMarkets[code] = v.map((x) => String(x).trim().toUpperCase()).filter(Boolean);
  }
  const m = JSON.parse(readFileSync(manifestPath, 'utf8'));
  if (!Array.isArray(m.markets)) {
    throw new Error('sync-api-workspace-acl: manifest.json missing markets array');
  }
  const order = m.markets.map((x) => String(x).trim().toUpperCase()).filter(Boolean);
  return { segmentToMarkets, order };
}

function emit({ segmentToMarkets, order }) {
  const orderLiteral = JSON.stringify(order);
  const segEntries = Object.entries(segmentToMarkets)
    .map(([code, ids]) => `  ${JSON.stringify(code)}: ${JSON.stringify(ids)} as const`)
    .join(',\n');
  const content = `/**
 * Generated by scripts/sync-api-workspace-acl.mjs from public/data — do not edit.
 * Lives under server/impl/ (outside api/) so Vercel does not treat it as a Serverless route.
 */
export const WORKSPACE_MANIFEST_MARKET_ORDER = ${orderLiteral} as readonly string[];

export const SEGMENT_TO_MARKETS: Record<string, readonly string[]> = {
${segEntries}
};
`;
  writeFileSync(outPath, content, 'utf8');
  console.log('sync-api-workspace-acl: wrote server/impl/_capacityWorkspaceAcl.data.ts');
}

emit(load());
